package com.joymeng.list;

public class TextNews {
	String tittle; // 公告标题
	String content; // 公告内容

	public String getTittle() {
		return tittle;
	}

	public void setTittle(String tittle) {
		this.tittle = tittle;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
